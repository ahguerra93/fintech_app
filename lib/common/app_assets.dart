/// Static asset path references for the app.
///
/// Usage:
/// ```dart
/// Image.asset(AppAssets.logoFinal)
/// Image.asset(AppAssets.logoOutlined)
/// ```
abstract final class AppAssets {
  static const String logoFinal = 'assets/images/logo_final.png';
  static const String logoOutlined = 'assets/images/logo_outlined.png';
}
